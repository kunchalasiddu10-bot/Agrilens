@app.route('/predict', methods=['POST'])
def predict():
    if 'file' not in request.files:
        return jsonify({'error': 'No file part'}), 400
    
    file = request.files['file']
    
    if file.filename == '':
        return jsonify({'error': 'No selected file'}), 400
        
    if file and allowed_file(file.filename):
        filename = secure_filename(file.filename)
        filepath = os.path.join(app.config['UPLOAD_FOLDER'], filename)
        file.save(filepath)
        
        # Run True AI analysis (internet connected, dynamic generation)
        lang = request.form.get('lang', 'English')
        analysis_result = analyze_image(filepath, lang=lang)
        
        if not analysis_result.get('success'):
            exact_error = analysis_result.get('error', 'Unknown AI Error')
            print(f"--- PREDICTION FAILED --- Error: {exact_error}")
            return jsonify({'error': f"AI Error: {exact_error}"}), 500
            
        prediction_name = analysis_result['prediction']
        confidence = analysis_result['confidence']
        detected_crop = analysis_result.get('detected_crop', 'Unknown')
        
        # Get the dynamically generated details straight from the AI's mind
        disease_details = analysis_result.get('disease_details', {})
        disease_details['crop'] = detected_crop # Ensure UI shows the dynamically detected crop
        
        # Format the display name if healthy
        if prediction_name == "Healthy":
            display_name = "No Disease Detected"
        else:
            display_name = prediction_name
            
        print(f"Image analyzed: {filename}")
        print(f"Detected Crop: {detected_crop}")
        print(f"Detected Disease: {display_name} ({confidence}%)")
        
        # Respond with JSON data to the frontend
        return jsonify({
            'success': True,
            'prediction': display_name,
            'confidence': confidence,
            'disease_details': disease_details,
            'image_url': f'/uploads/{filename}'
        })
        
    return jsonify({'error': 'Invalid file type. Please upload a JPG or PNG image.'}), 400

