@app.route('/calendar_schedule', methods=['POST'])
def calendar_schedule():
    data = request.get_json()
    crop = data.get('crop', '')
    date = data.get('date', '')
    lang = data.get('lang', 'English')
    
    if not crop or not date:
        return jsonify({'error': 'Missing crop or date'}), 400
        
    try:
        import json
        load_dotenv(override=True)
        api_key = os.getenv("GEMINI_API_KEY")
        client = genai.Client(api_key=api_key)
        
        prompt = f"""You are an expert AI agricultural planner. The user wants to plant the crop '{crop}' starting on the date '{date}'.
Generate a complete timeline of tasks from planting to harvesting.
IMPORTANT: You MUST reply entirely in the {lang} language (except for JSON keys).

Return ONLY a raw JSON object string with this exact schema (no markdown blocks, no extra text):
{{
    "timeline": [
        {{"date": "calculated date based on {date}", "stage": "Stage Name", "task": "Detailed description of what to do (fertilizer, watering, etc)"}},
        {{"date": "next calculated date", "stage": "Next Stage", "task": "Description of next task"}}
    ]
}}
Ensure there are at least 5-8 chronological stages."""
        
        response = client.models.generate_content(
            model='gemini-2.5-flash', 
            contents=prompt,
            config=genai.types.GenerateContentConfig(temperature=0.2)
        )
        
        response_text = response.text.strip()
        if response_text.startswith("```json"): response_text = response_text[7:-3].strip()
        elif response_text.startswith("```"): response_text = response_text[3:-3].strip()
            
        schedule = json.loads(response_text)
        return jsonify({'success': True, 'timeline': schedule.get('timeline', [])})
    except Exception as e:
        print(f"Calendar error: {e}")
        return jsonify({'success': False, 'error': str(e)})

